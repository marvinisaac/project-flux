const grid = () => {
    const cellCount = 1000;
    const xAspectRatio = 16;
    const yAspectRatio = 9;
    const aspectRatio = xAspectRatio / yAspectRatio;
    const xCount = Math.round(Math.sqrt(aspectRatio * cellCount))
    const xMin = 50
    const xMax = width - 50;
    const yCount = Math.round(cellCount / xCount);
    const yMin = 50;
    const yMax = height - 50;

    const points = [];
    const xIncrement = (xMax - xMin) / xCount;
    const yIncrement = (yMax - yMin) / yCount;
    for (let x = 1; x < xCount; x++) {
        for (let y = 1; y < yCount; y++) {
            points.push([
                Math.round(xMin + (x * xIncrement)),
                Math.round(yMin + (y * yIncrement)),
            ]);
        }
    }

    const jitter = Math.round((xIncrement + yIncrement) / 6);
    const pointsJitter = addJitter(points, jitter);

    return pointsJitter;
}

const addJitter = (points, jitter) => {
    points.forEach(point => {
        point[0] += getRandom(-1 * jitter, jitter);
        point[1] += getRandom(-1 * jitter, jitter);
    });
    return points;
}

